@router.message(TrainerStates.waiting_for_answer)
async def check_answer(message: types.Message, state: FSMContext):
    """
    Check user's answer
    """
    # Handle menu command
    if message.text == "/menu":
        await message.answer("–ü–æ–≤–µ—Ä–Ω–µ–Ω–Ω—è –¥–æ –≥–æ–ª–æ–≤–Ω–æ–≥–æ –º–µ–Ω—é", reply_markup=get_main_keyboard())
        await state.clear()
        return

    data = await state.get_data()
    current_question = data.get("current_question", 0)
    correct_answers = data.get("correct_answers", 0)
    user_answers = data.get("user_answers", [])
    selected_questions = data.get("selected_questions", [])
    subject = data.get("subject", "")
    question = selected_questions[current_question]

    # Save user's answer
    user_answers.append({
        "question": question["question"],
        "user_answer": message.text,
        "correct_answer": question["options"][question["correct"]],
        "is_correct": message.text == question["options"][question["correct"]]
    })

    # Check answer
    is_correct = message.text == question["options"][question["correct"]]
    if is_correct:
        correct_answers += 1
        await message.answer("‚úÖ –ü—Ä–∞–≤–∏–ª—å–Ω–æ!\n\n" + question["explanation"])
    else:
        await message.answer(
            f"‚ùå –ù–µ–ø—Ä–∞–≤–∏–ª—å–Ω–æ!\n–ü—Ä–∞–≤–∏–ª—å–Ω–∞ –≤—ñ–¥–ø–æ–≤—ñ–¥—å: {question['options'][question['correct']]}\n\n" + 
            question["explanation"]
        )

    # Move to next question
    current_question += 1
